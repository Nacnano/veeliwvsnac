package game;

/**
 * The Terrain enum represents different types of terrain used for creating the map.
 */
public enum Terrain {
    /**
     * Represents a plain terrain.
     */
    PLAIN,

    /**
     * Represents a water terrain.
     */
    WATER,

    /**
     * Represents a mountain terrain.
     */
    MOUNTAIN,

    /**
     * Represents a forest terrain.
     */
    FOREST
}
